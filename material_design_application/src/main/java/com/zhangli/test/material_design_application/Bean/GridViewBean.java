package com.zhangli.test.material_design_application.Bean;

public class GridViewBean {
    private int image;
    private String name;

    public GridViewBean(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
